<script>
    $(document).ready(function(e) {
        $('#error_msg').hide(); 
            $('#error_view').html(""); 
        $('#register_submit').click(function(e){
            $('#age').html(""); 
            $('#error_msg').hide(); 
            $('#error_view').html(""); 
            var dob1 =$('#dob').val();
            dob = new Date(dob1);
            var today = new Date();
            var age = Math.floor((today-dob) / (365.25 * 24 * 60 * 60 * 1000));
            $('#age').html('You are '+age+' years old');
            if(age<12){
                $('#error_msg').show(); 
                $('#error_view').html("Minimum Age Limit is 12 to entroll.");
                e.preventDefault();
            }else{
                $('#error_msg').hide(); 
                $('#error_view').html("");            }
            //e.preventDefault();
        });
    });
    
</script>
